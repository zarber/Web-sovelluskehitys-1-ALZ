const sum = (number1, number2) => {
  return number1 + number2;
};

const subs = (number1, number2) => {
  return number1 - number2;
};

const mult = (number1, number2) => {
  return number1 * number2;
};

const div = (number1, number2) => {
  return number1 / number2;
};

module.exports = {
  sum,
  subs,
  mult,
  div,
};
